% --- Executes just before process_metafile is made visible.
function process_metafile_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to process_metafile (see VARARGIN)

if(nargin > 3)
    for index = 1:2:(nargin-3),
        if nargin-3==index, break, end
        switch lower(varargin{index})
            case 'base_dir', base_dir = varargin{index+1};
            case 'include_outline', include_outline = varargin{index+1};
            case 'outline_color', outline_color = varargin{index+1};
            case 'zero_outside', zero_outside = varargin{index+1};
            case 'base_mass_for_alignment', base_mass_for_alignment = varargin{index+1};
            case 'all_handles', all_handles = varargin{index+1};
            %case 'additional_settings', additional_settings = varargin{index+1};
        end
    end
else
    base_dir = pwd;
    include_outline = 0;
    outline_color='w';
    zero_outside = 0;
    base_mass_for_alignment = 'Esi';
    all_handles = [];
    %additional_settings = 0;
end
base_dir = fixdir(base_dir);
set(handles.edit1,'string',base_dir);
set(handles.edit3,'string',[base_dir,'metafile.txt']);
set(handles.edit22,'string',outline_color);
set(handles.checkbox5,'value',include_outline);
set(handles.checkbox6,'value',zero_outside);
handles.base_mass_for_alignment = base_mass_for_alignment;
handles.all_handles = all_handles;
%handles.additional_settings = additional_settings;

% default corrections settings
global correction_settings;
% make sure that when the process_metafile GUI is open, the correction and
% rate calculation options are always set to 0 (=not applied) by default to
% avoid unintentional consequences
correction_settings.correction_apply = zeros(1,6);
correction_settings.calculate_rate = zeros(1,6);
handles.correction_settings = correction_settings;

% Choose default command line output for process_metafile
handles.output = hObject;

handles = update_gui_fontsize(handles);

% Update handles structure
guidata(hObject, handles);

% UIWAIT makes process_metafile wait for user response (see UIRESUME)
% uiwait(handles.figure1);


